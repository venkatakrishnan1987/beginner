# ================================================================================
# VARIABLES & USER INPUT
# ----------------------------------------
# This section shows how to accept user input using the `input()` function.
# We then combine it with a predefined variable to display a complete sentence.
# ================================================================================


# Dynamic Value: Ask the user to enter their name and store it in a variable.
name = input("Enter Your Name:")

# Hardcoded value
country = "Germany"

# Combine both variables
print(name, "comes from", country)
